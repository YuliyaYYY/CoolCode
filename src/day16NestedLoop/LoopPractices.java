package day16NestedLoop;

public class LoopPractices {

    public static void main(String[] args) {

        for (int i = 1; i < 6 ; i++) {
            System.out.println("Hello" + i);

        }

        System.out.println("-------------------------------------");

        for (int i = 0; i < 5; i++) {
            System.out.println("Hello" + i);

        }

        System.out.println("--------------------------------------");

        int j = 0;
        while(j < 5) {
            System.out.println("Hello" + j);
            j++;
        }

        System.out.println("------------------------------");

         int k = 0;
         do {
             System.out.println("Hello" + k);
             k++;
         }while(k < 5);
         }

        }



