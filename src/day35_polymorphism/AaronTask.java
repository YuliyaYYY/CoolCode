package day35_polymorphism;

public class AaronTask {

    public static void main(String[] args) {

        for (int i = 'A'/'A'; i < 'e'; i++) {
            System.out.print(i + " ");
        }

        System.out.println("---------------------------------------");

        int one = 1;
        int hundred = 100;

        for (int i = one; i <= hundred; i++) {
            System.out.print(i + " ");

        }
    }
}
